// String
let empName = 'Rajan';
console.log(`Emp Name : ${empName}`);

// Number
let empSalary = 50000;
console.log(`empSalary : ${empSalary}`);

// Boolean
let isPermanent = false;
console.log(`is Permanent : ${isPermanent}`);

// undefined
let name;
console.log(`name : ${name}`);

// null
let test = null; // dummy value
console.log(`Test : ${test}`);

// Re-assignment of variables
let a;
console.log(a); // undefined

a = 10;
console.log(a); // 10

a = 'test';
console.log(a); // test

a = true;
console.log(a); // true

a = null;
console.log(a); // null

